# Create a list using[]

a=[2,3,4,3,6,4,"satya"]
# print the list using print() function
print(a)

# Access using index using  a[0], a[1], a[2]
print(a[2])

# Change the value of list using  this function
a[0]=34
print(a)

# we can create a list with items of different types
c=[34,"harry",False,3.2]
print(c)

# List Slicing

friends=["Deepal","Dukhi","Ritam","Atul","Subhash",12]
print(friends[0:3])
print(friends[3:6])
print(friends[-5:])
print(friends[4])